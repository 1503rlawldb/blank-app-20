@st.cache_data
def generate_sea_level_data():
    """1880ë…„ë¶€í„° 2025ë…„ê¹Œì§€ì˜ í•´ìˆ˜ë©´ ìƒìŠ¹ ë°ì´í„°ë¥¼ ì‹œë®¬ë ˆì´ì…˜í•©ë‹ˆë‹¤."""
    years = np.arange(1880, 2026)
    # ì‹œê°„ì´ ì§€ë‚ ìˆ˜ë¡ ìƒìŠ¹í­ì´ ì»¤ì§€ëŠ” ë¹„ì„ í˜•ì  ì¦ê°€ë¥¼ ì‹œë®¬ë ˆì´ì…˜
