@st.cache_data
def generate_map_data(year):
    """ì„ íƒëœ ì—°ë„ì— ë”°ë¼ ê¸°ì˜¨ í¸ì°¨ ê°•ë„ê°€ ë‹¬ë¼ì§€ëŠ” ì§€ë„ ë°ì´í„°ë¥¼ ìƒì„±í•©ë‹ˆë‹¤."""
