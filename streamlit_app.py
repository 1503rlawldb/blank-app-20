    intensity_factor = max(0.1, (year - 1880) / (2025 - 1880))
    num_points = 1000
    df_map = pd.DataFrame({
        "lat": np.random.uniform(-60, 80, num_points),
        "lon": np.random.uniform(-180, 180, num_points),
        "anomaly": np.random.uniform(-2, 2, num_points) * intensity_factor
    })
    return df_map

